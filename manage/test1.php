<figure><img src="http://cdn.baabinz.com/upload/files/Teng1st/enhanced_10835_1458240863_13.jpg" /><figcaption class="op-vertical-center"><h1 class="op-vertical-above op-center">ภาพจาก</h1><cite class="op-vertical-below op-right">buzzfeed</cite></figcaption></figure>
