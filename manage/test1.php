<strong>แค่อยากเตือนให้ผู้หญิงทุกคน"จดทะเบียนสมรส"นะครับ ใบๆเดียวสำคัญมาก ถ้าไม่มีคุณก็ไม่ใช่ภรรยาเค้าครับ เป็นมากสุดก็แค่คู่นอน</strong>
ผมอยากเตือนจริงๆ คือ เห็นสาวๆสมัยนี้บางคนมีความคิด liberal ไม่จดทะเบียนก็ได้ บางคนถือว่าชั้นคบมาก่อน คนนี้แฟนชั้น หรืออยู่ด้วยกันก็คือสามีแล้วนะยะ หรือบางคนผู้ชายเป็นแค่แฟน ไปเรียกเค้าว่าสามีแล้วยังมีเลย หรือบางคนถูกผู้ชายหลอกว่าไม่อยากจดทะเบียนเพราะเหตุผลทางธุรกิจโน่นนี่<br />ผมอยากเตือนและอธิบายไว้ตรงนี้นะครับ<br /><br />1. ถ้าไม่มีทะเบียนสมรส ต่อให้คบกันมานาน กินอยู่กันแล้ว หรือถึงขั้นจัดงานแต่งกลางอำเภอ มีกำนัน ผู้ใหญ่บ้าน คนมารวมงานเป็นร้อย คุณก็ยังไม่ใช่ภรรยาที่ถูกต้องตามกฎหมายของเขา และเขาก็ไม่ใช่สามีที่ถูกต้องตามกฎหมายของคุณ เป็นได้อย่างมากก็แค่ แฟน หรือ คู่นอน<br /><br />2. ดังนั้น ต่อให้สังคมรับรู้ว่าคุณคบกับคนๆนี้อยู่ หรือกินอยู่ด้วยกันอยู่ แต่คุณ "ไม่มีทะเบียนสมรส" และผู้ชายเกิดไปชอบพอกับผู้หญิงคนใหม่<u>คุณก็ "ทำอะไรไม่ได้"</u><br /><br />3. และยิ่งถ้าเขาไปจดทะเบียนสมรสกับผู้หญิงอีกคน คุณก็จะกลายเป็น "เมียน้อยทันที"<br /><br />4. ดังนั้น<u>ประเด็น "ไม่ได้อยู่ที่ใครมาก่อนหลัง แต่อยู่ที่ว่าสุดท้ายใครได้ทะเบียนสมรสไป"</u><br /><br />5. ผู้หญิงบางคนเป็นแค่แฟน แต่จับได้ว่าผู้ชายไปคบคนอื่น หรือ ไปมีกิ๊ก ก็ไประรานเขา ระวังผู้ชายไปเลือกคนใหม่ แล้วสุดท้ายก็แต่งงานจดทะเบียนกับอีกคน ที่คุณไประรานเขา จะผิดฎหมายนะครับ<br /><br />6. ถ้าไม่มีทะเบียนสมรส ต่อให้กินอยู่กับเขามานานหลายปี ช่วยเขาทุกอย่างยามลำบาก<u>แต่ถ้าเลิกกันคุณก็จะไม่ได้อะไรกลับแม้แต่สตางค์แดงเดียว</u>เค้าไปมีกิ๊กคุณก็ทำอะไรไม่ได้<br /><br />7. ผู้ชายบางคนอ้างว่าไม่จดทะเบียนเพราะเหตุผลทางธุรกิจโน่นนี่ บอกว่าให้มากินอยู่ด้วยกันนี่แหละแต่ไม่จดทะเบียนนะ อยากบอกว่าถูกหลอกแล้วครับถ้าเจองี้ คือ บางคนมันก็ยังไม่อยากจริงจัง อยากมีคู่นอน มีคนอยู่เป็นเพื่อนแต่ไม่พร้อมผูกมัดมากกว่า ไม่ก็มีเมียอยู่แล้วแล้วมาหลอก ถ้าเค้ารักเราจริง กะแค่จดทะเบียนสมรส ไปที่อำเภอ ทำให้มันโปร่งใส ทำไมจะทำไม่ได้ครับ? กลัวโดนแบ่งทรัพย์สินเหรอถ้าหย่า ไม่มั่นใจในตัวเราเหรอ? ระดับอภิมหาเศรษฐีหรือคนสำคัญระดับโลก คุณบุญชัย เจ้าสัวธนิน คุณสงกรานต์แฟนคุณแอฟ คุณต๊อดแฟนคุณนุ่น นี่ธุรกิจใหญ่โต ระดับอภิมหาเศรษฐี เค้ายังจดทะเบียนสมรสได้ แล้วบางคนธุรกิจธรรมดา ไม่ได้ระดับพันล้าน หมื่นล้าน ทำไมจะจดไม่ได้ครับ<br /><br /><br />เวลาผมเห็นผู้หญิงมาตั้งคำถามใ