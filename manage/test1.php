<p class="subbuzz_name" data-subbuzz-id="8243111">ดัดลีย์จึงลงมือค้นคว้าวิจัยเกี่ยวกับที่ดัดฟันแบบใส่ และจากข้อมูลที่เขาได้มาจากอินเตอร์เน็ตเขาได้สังเกตุเห็นอย่างหนึ่ง นั่นคือเจ้าพวกนี้มันสามารถสร้างขึ้นมาเองได้ด้วยการพิมพ์ออกมาแบบ 3มิติ ด้วยเครื่องพิมพ์ 3D ซึ่งสำหรับเขาไม่ต้องไปหาที่ไหนไกลเลย เพราะเขาเป็นนักศึกษาสาขาออกแบบด้วยระบบดิจิตอลอยู่แล้ว เขาจึงสามารถใช้เครื่องนี้ได้อย่างอิสระ
<p class="subbuzz_name" data-subbuzz-id="8243111"><figure><img src="http://cdn.baabinz.com/upload/files/Teng1st/enhanced_18599_1458240720_2.png" /><figcaption class="op-vertical-center"><h1 class="op-vertical-above op-center">ภาพจาก</h1><cite class="op-vertical-below op-right">buzzfeed</cite></figcaption></figure>
